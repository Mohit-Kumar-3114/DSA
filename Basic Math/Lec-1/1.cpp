// Understanding the basics of cpp

// It is a preprocessor directive in C++ that includes the Input-Output Stream (iostream) library in your program.
// This library is essential for handling input and output operations, such as reading from the standard input
// (keyboard) and writing to the standard output (console).
#include <iostream>




// It allows you to use standard library functions (like cout, cin, endl) without prefixing them with std::.
using namespace std; 




// Starting point of the execution 
int main(){
    // cout is an object with insertion operator(<<) to print anything.
    cout<<"hello world";
    // Return 0 indicates that the program has successfully executed.
    return 0;
}




